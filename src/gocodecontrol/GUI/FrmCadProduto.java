    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        Produto prod = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        
        String nmProduto = txtProduto.getText().trim();
        Float valorProduto = parseFloat(txtValor.getText());
        Boolean ativo = parseBoolean(checkAtivo.getText());
        int qtde = parseInt(txtQtde.getText());
        
        prod.setNm_produto(nmProduto);
        prod.setValor(valorProduto);
        prod.setAtivo(ativo);
        prod.setQtde(qtde);

        if (dao.inserir(prod)) {
            JOptionPane.showMessageDialog(this, "Produto cadastrado com Ãªxito");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar o Produto.\n", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        //JOptionPane.showMessageDialog (null, "Cadastro realizado!");
    }//GEN-LAST:event_btnCadastrarActionPerformed

